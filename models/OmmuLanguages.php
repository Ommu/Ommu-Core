	/**
	 * User get information
	 */
	public static function getInfo($id, $column=null)
	{
		if($column != null) {
			$model = self::model()->findByPk($id, array(
				'select' => $column,
			));
 			if(count(explode(',', $column)) == 1)
 				return $model->$column;
 			else
 				return $model;
			
		} else {
			$model = self::model()->findByPk($id);
			return $model;
		}
	}

	/**
	 * getLanguages
	 */
	public static function getLanguage($actived=null, $view='id', $array=true) 
	{
		$criteria=new CDbCriteria;
		if($actived != null)
			$criteria->compare('t.actived',$actived);
		
		$model = self::model()->findAll($criteria);

		if($array == true) {
			$items = array();
			if($model != null) {
				foreach($model as $key => $val) {
					if($view == 'id')
						$items[$val->language_id] = $val->name;
					else if($view == 'code')
						$items[$val->code] = $val->name;
				}
				return $items;
			} else
				return false;
		} else
			return $model;
	}

	/**
	 * getDefault
	 */
	public static function getDefault($select=null) 
	{
		$model = self::model()->findByAttributes(array('default' => 1));
		if($select == null)
			$return = $model->language_id;
		else
			$return = $model->$select;
			
		return $return;
	}

	/**
	 * before validate attributes
	 */
	protected function beforeValidate() 
	{
		if(parent::beforeValidate()) {
			if($this->isNewRecord) {
				if($this->default == 1)
					$this->actived = 1;
				$this->creation_id = !Yii::app()->user->isGuest ? Yii::app()->user->id : null;
			} else
				$this->modified_id = !Yii::app()->user->isGuest ? Yii::app()->user->id : null;
		}
		return true;
	}
	
	/**
	 * before save attributes
	 */
	protected function beforeSave() 
	{
		if(parent::beforeSave()) {
			// Language set to default
			if ($this->default == 1) {
				self::model()->updateAll(array(
					'default' => 0,	
				));
				$this->default = 1;
			}
		}
		return true;
	}

